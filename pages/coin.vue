<p>To make a new word is to make a new world. We coin the future moon, big and gold and haunted by its own sky—moonmoon, we say—like I say maemae or wrenwren to my hens in the coop because somehow doubling the size of the name is also a dimunition, an intimacy. They live together, outside, because I don’t keep chickens in the house, even though they are closer to pets than wild animals. Even the moon scares them with its slight light. What we let stay or what we refuse is so conventional. We say we give love reciprocally, to those who deserve it, who earn it, but truly we don’t fuss over definitions. We want to believe that all caretaking is caring, that we are cleaved and crinkled by a process that reveals us, the shiny pennies that we are—or were—the quarters edged in pink, trout-lipped glint. We make a life from what we coin—we double it, we lose it. We smuggle budwood across state lines. We grow trees on window sills until we live inside forests again. Until we’re sure enough luck has piled at the bottom of pools. All night while we sleep, the moon shines. The hearts of the chickens beat in their box in the dark.</p>

