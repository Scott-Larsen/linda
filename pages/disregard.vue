<h2 class="entry-content">	
Disregard
</h2>

<p>What words form through the open gate,</br>
which we take at face value. Trails meant</p>

<p>for walking. It is easy to pitch a body forward</br>
and think of wool stars, crushed underfoot,</p>

<p>the smell of the present. To believe</br>
as we move that we are not moving</p>

<p>elsewhere, to feel the order of words exists</br>
in us only. We disregard branches that beckon</p>

<p>back, that dissolve in a foregone distance.</br>
We ignore what the hunters know better</p>

<p>than most: flowers open then shut.</br>
At the end of each branch, a terminal bud.</p>

